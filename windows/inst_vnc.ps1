function Install-Vnc {

    Set-ExecutionPolicy Bypass -Scope Process -Force; [System.Net.ServicePointManager]::SecurityProtocol = [System.Net.ServicePointManager]::SecurityProtocol -bor 3072; Invoke-Expression ((New-Object System.Net.WebClient).DownloadString('https://community.chocolatey.org/install.ps1'))

    $EXTRA_PORT = '"5999:1600X900+0+X"'

    $ARGS_1 = "SET_USEVNCAUTHENTICATION=1 VALUE_OF_USEVNCAUTHENTICATION=1 SET_PASSWORD=1 VALUE_OF_PASSWORD=mainpass"
    $ARGS_2 = "SET_VIEWONLYPASSWORD=1 VALUE_OF_VIEWONLYPASSWORD=viewpass SET_USECONTROLAUTHENTICATION=1 SERVER_REGISTER_AS_SERVICE=1"
    $ARGS_3 = "SERVER_ADD_FIREWALL_EXCEPTION=1 SET_ALWAYSSHARED=1 SET_EXTRAPORTS=$EXTRA_PORT"

    choco upgrade tightvnc -y --installArguments "$ARGS_1 $ARGS_2 $ARGS_3"
    choco install git.install -y --params "'/GitAndUnixToolsOnPath /NoAutoCrlf'"

    return $true

}

Install-Vnc
